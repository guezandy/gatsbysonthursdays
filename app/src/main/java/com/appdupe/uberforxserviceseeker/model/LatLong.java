package com.appdupe.uberforxserviceseeker.model;

public class LatLong {

	private double lat, lng;

	public LatLong() {
	}

	public double getLat() {
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}

	public double getLng() {
		return lng;
	}

	public void setLng(double lng) {
		this.lng = lng;
	}
}
